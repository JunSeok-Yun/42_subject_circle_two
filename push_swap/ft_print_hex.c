#include "ft_printf.h"

static int	hex_len(unsigned int num)
{
	int	len;

	len = 0;
	while (num != 0)
	{
		len++;
		num /= 16;
	}
	return (len);
}

static void	print_nbr_hex(unsigned int num, const char format)
{
	if (num >= 16)
	{
		print_nbr_hex(num / 16, format);
		print_nbr_hex(num % 16, format);
	}
	else
	{
		if (num <= 9)
			ft_putchar(num + '0');
		else
		{
			if (format == 'x')
				ft_putchar(num - 10 + 'a');
			else if (format == 'X')
				ft_putchar(num - 10 + 'A');
		}
	}
}

int	ft_print_hex(unsigned int num, const char format)
{
	if (num == 0)
		return (ft_putchar('0'));
	else
		print_nbr_hex(num, format);
	return (hex_len(num));
}
